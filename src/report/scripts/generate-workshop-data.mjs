import { readdir, readFile, writeFile, access } from 'fs/promises'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))

const WORKSHOP_DIR = join(__dirname, '../../react/docs/workshop-guidance')
const OUTPUT_FILE = join(__dirname, '../src/config/workshop-data.ts')

const PILLAR_MAP = {
  identity: 'Identidade',
  devices: 'Dispositivos',
  network: 'Rede',
  data: 'Dados',
  infrastructure: 'Infraestrutura',
  devsecops: 'DevSecOps',
  AI: 'Inteligência Artificial',
  securityoperations: 'Operações de Segurança',
}

const EFFORT_PT = { Low: 'Baixo', Medium: 'Médio', High: 'Alto', Baixo: 'Baixo', Médio: 'Médio', Alto: 'Alto' }
const IMPACT_PT = { Low: 'Baixo', Medium: 'Médio', High: 'Alto', Baixo: 'Baixo', Médio: 'Médio', Alto: 'Alto' }

function capitalize(s) {
  return s.charAt(0).toUpperCase() + s.slice(1).toLowerCase()
}

function normalize(content) {
  // Strip BOM, normalize Windows line endings
  return content.replace(/^﻿/, '').replace(/\r\n/g, '\n').replace(/\r/g, '\n')
}

function parseMarkdown(rawContent, filename, pillar) {
  const content = normalize(rawContent)

  // Match first heading (any level), allowing optional leading whitespace
  const titleMatch = content.match(/^[ \t]*#+[ \t]+(.+)/m)
  const title = titleMatch ? titleMatch[1].trim() : filename

  // Match effort — handles em-dash separators and descriptions after the level
  const effortMatch = content.match(/\*\*(?:Implementation Effort|Esforço de Implementação):\*\*[^\n]*(Low|Medium|High|Baixo|Médio|Alto)/i)
  const effortEn = effortMatch ? capitalize(effortMatch[1]) : null
  const effort = effortEn ? (EFFORT_PT[effortEn] ?? null) : null

  // Match impact
  const impactMatch = content.match(/\*\*(?:User Impact|Impacto ao Usuário):\*\*[^\n]*(Low|Medium|High|Baixo|Médio|Alto)/i)
  const impactEn = impactMatch ? capitalize(impactMatch[1]) : null
  const impact = impactEn ? (IMPACT_PT[impactEn] ?? null) : null

  return { filename, pillar, title, effort, impact, content }
}

async function dirExists(p) {
  try {
    await access(p)
    return true
  } catch {
    return false
  }
}

async function main() {
  const result = []
  let totalFiles = 0
  let missingTitles = 0

  for (const [dir, pillarLabel] of Object.entries(PILLAR_MAP)) {
    const dirPath = join(WORKSHOP_DIR, dir)
    if (!(await dirExists(dirPath))) {
      console.warn(`  [skip] Directory not found: ${dir}`)
      continue
    }

    const entries = await readdir(dirPath)
    const mdFiles = entries.filter(f => f.endsWith('.md') && !f.startsWith('_'))

    for (const file of mdFiles) {
      const rawContent = await readFile(join(dirPath, file), 'utf-8')
      const filename = file.replace(/\.md$/, '')
      const parsed = parseMarkdown(rawContent, filename, pillarLabel)
      if (parsed.title === filename) missingTitles++
      result.push(parsed)
      totalFiles++
    }

    console.log(`  ${pillarLabel}: ${mdFiles.length} guias`)
  }

  const pillars = [...new Set(Object.values(PILLAR_MAP))]
  const pillarUnionType = pillars.map(p => `'${p}'`).join(' | ')

  const output = `// Auto-generated by scripts/generate-workshop-data.mjs — do not edit manually
// Run: npm run generate-workshop

export type WorkshopPillar = ${pillarUnionType}

export type WorkshopLevel = 'Baixo' | 'Médio' | 'Alto'

export interface WorkshopGuide {
  filename: string
  pillar: WorkshopPillar
  title: string
  effort: WorkshopLevel | null
  impact: WorkshopLevel | null
  content: string
}

export const workshopData: WorkshopGuide[] = ${JSON.stringify(result, null, 2)}
`

  await writeFile(OUTPUT_FILE, output, 'utf-8')
  console.log(`\nGerado: ${OUTPUT_FILE}`)
  console.log(`Total: ${totalFiles} guias de implementação`)
  if (missingTitles > 0) {
    console.warn(`Aviso: ${missingTitles} guias sem título H1 — usando filename como fallback`)
  }
}

main().catch(err => {
  console.error('Erro ao gerar workshop-data.ts:', err)
  process.exit(1)
})
